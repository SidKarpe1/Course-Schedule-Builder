    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    public void rebuildCourseCodeComboBoxes() {
        ArrayList<String> courses = CourseQueries.getAllCourseCode();
        currentCoursesComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        selectClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        studentDropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        adminDropClassComboBox.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        if (courses.size() > 0) {
            currentCourse = courses.get(0);
        } else {
            currentCourse = "None";
        }
    }
    public void rebuildStudentComboBoxes() {
    ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
    ArrayList<String> studentIDs = new ArrayList<>(); // ArrayList to hold student IDs

    for (StudentEntry student : students) {
        studentIDs.add(student.getStudentID()); // Add studentID attribute to the new list
    }

    selectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentIDs.toArray()));
    studentScheduleComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentIDs.toArray()));
    studentDropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentIDs.toArray()));
    adminDropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentIDs.toArray()));

    if (!studentIDs.isEmpty()) {
        currentStudentID = studentIDs.get(0); // Assuming currentStudentID is a string variable
    } else {
        currentCourse = "None";
    }
    }
