    private void adminDropClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropClassButtonActionPerformed
        // TODO add your handling code here:
        // drop students, get enrolled students and waitlisted students, print that they have been removed in the textArea
        String currentClass = adminDropClassComboBox.getSelectedItem().toString();
        String currentSemester = currentSemesterComboBox.getSelectedItem().toString();
        ArrayList<StudentEntry> studentsScheduled = MultiTableQueries.getScheduledStudentByClass(currentSemester, currentClass);
        adminDropClassTextArea.setText("Scheduled students dropped from the course\n");
        for (StudentEntry student: studentsScheduled){
            adminDropClassTextArea.append(student.getFirstName() + "\t" + student.getLastName() + "\t" + student.getStudentID()+"\n");
            
        }
        
        ArrayList<StudentEntry> studentsWaitlisted = MultiTableQueries.getWaitlistedStudentByClass(currentSemester, currentClass);
        adminDropClassTextArea.append("Waitedlisted students dropped from the course\n");
        for (StudentEntry student: studentsWaitlisted){
            adminDropClassTextArea.append(student.getFirstName() + "\t" + student.getLastName() + "\t" + student.getStudentID()+"\n");
            
        }
        ClassQueries.dropClass(currentSemester, currentClass);
        ScheduleQueries.dropScheduleByCourse(currentSemester, currentClass);
        rebuildCourseCodeComboBoxes();
    }//GEN-LAST:event_adminDropClassButtonActionPerformed

