    private void studentScheduleSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentScheduleSubmitButtonActionPerformed
        // TODO add your handling code here:
        String student = (String) studentScheduleComboBox.getSelectedItem();
        String semester = currentSemesterComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> classes= ScheduleQueries.getScheduleByStudent(semester, student);
        studentScheduleTable.setText("CourseCode\tStatus\n");
        for (ScheduleEntry eachClass : classes) {
            studentScheduleTable.append(eachClass.getCourseCode() + "\t" + eachClass.getStatus() + "\n");

        }

    }//GEN-LAST:event_studentScheduleSubmitButtonActionPerformed

    private void scheduleClassesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassesSubmitButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = (String) selectClassComboBox.getSelectedItem();
        String semester = currentSemesterComboBox.getSelectedItem().toString();
        ClassEntry section = (ClassEntry) ClassQueries.getClass(courseCode, semester);
        String currentStatus = "";
        String studentID = (String) selectStudentComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry) StudentQueries.getStudent(studentID);

        if (ClassQueries.getClassSeats(semester, courseCode) > (ScheduleQueries.getScheduledStudentCount(semester, courseCode)))
        {
            currentStatus = "Scheduled";
            scheduleClassesStatusLabel.setText(student.getFirstName() + " " + student.getLastName() + " has been scheduled for " + courseCode);
        }

        else {
            currentStatus = "Wait Listed";
            scheduleClassesStatusLabel.setText(student.getFirstName()  + " " + student.getLastName() + " has been waitedlisted for " + courseCode);
        }
        ScheduleEntry newSchedule = new ScheduleEntry(semester, courseCode, student.getStudentID(), currentStatus, new java.sql.Timestamp(System.currentTimeMillis()));
        ScheduleQueries.addStudentEntry(newSchedule);

    }//GEN-LAST:event_scheduleClassesSubmitButtonActionPerformed

